SELECT 
    dp.id AS product_id,
    ds.id AS store_id,
    dd.id AS date_id,
    dp.default_price AS price,
    ROUND(RAND() * dp.default_price, 2) AS discount,
    ROUND(RAND() * 100) + 1 AS quantity
FROM 
    dim_products dp
CROSS JOIN
    dim_stores ds
CROSS JOIN
    dim_dates dd;


INSERT INTO fact_average_basket (total_amount, number_of_items, date_id, store_id, customer_id)
SELECT
    SUM(fs.price * fs.quantity) AS total_amount,
    COUNT(*) AS number_of_items,
    dd.id AS date_id,
    ds.id AS store_id,
    dc.id AS customer_id
FROM (select * from fact_sales LIMIT 20000) fs
JOIN
    dim_dates dd ON fs.date_id = dd.id
JOIN
    dim_stores ds ON fs.store_id = ds.id
JOIN
    dim_customers dc ON dc.id = ROUND(RAND() * (SELECT MAX(id) FROM dim_customers))
GROUP BY
    fs.id, dd.id, ds.id, dc.id;