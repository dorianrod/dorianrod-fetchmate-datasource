UPDATE dim_products 
SET default_price = 
    CASE 
        WHEN category = 'Fruits' THEN ROUND(RAND() * 5 + 1, 2) -- Prix aléatoire entre 1 et 6 pour les fruits
        WHEN category = 'Vegetables' THEN ROUND(RAND() * 3 + 1, 2) -- Prix aléatoire entre 1 et 4 pour les légumes
        WHEN category = 'Meat' THEN ROUND(RAND() * 10 + 5, 2) -- Prix aléatoire entre 5 et 15 pour la viande
        WHEN category = 'Fish' THEN ROUND(RAND() * 15 + 10, 2) -- Prix aléatoire entre 10 et 25 pour le poisson
        WHEN category = 'Seafood' THEN ROUND(RAND() * 20 + 15, 2) -- Prix aléatoire entre 15 et 35 pour les fruits de mer
        WHEN category = 'Dairy' THEN ROUND(RAND() * 4 + 2, 2) -- Prix aléatoire entre 2 et 6 pour les produits laitiers
        WHEN category = 'Bakery' THEN ROUND(RAND() * 4 + 2, 2) -- Prix aléatoire entre 2 et 6 pour la boulangerie
        WHEN category = 'Snacks' THEN ROUND(RAND() * 3 + 2, 2) -- Prix aléatoire entre 2 et 5 pour les collations
        WHEN category = 'Sweets' THEN ROUND(RAND() * 5 + 3, 2) -- Prix aléatoire entre 3 et 8 pour les sucreries
        WHEN category = 'Frozen Desserts' THEN ROUND(RAND() * 6 + 4, 2) -- Prix aléatoire entre 4 et 10 pour les desserts glacés
        WHEN category = 'Frozen Food' THEN ROUND(RAND() * 7 + 5, 2) -- Prix aléatoire entre 5 et 12 pour les aliments surgelés
        WHEN category = 'Pantry' THEN ROUND(RAND() * 2 + 1, 2) -- Prix aléatoire entre 1 et 3 pour les articles d'épicerie
        WHEN category = 'Condiments' THEN ROUND(RAND() * 3 + 1, 2) -- Prix aléatoire entre 1 et 4 pour les condiments
        WHEN category = 'Personal Care' THEN ROUND(RAND() * 10 + 5, 2) -- Prix aléatoire entre 5 et 15 pour les produits de soins personnels
        WHEN category = 'Household' THEN ROUND(RAND() * 6 + 4, 2) -- Prix aléatoire entre 4 et 10 pour les articles ménagers
        WHEN category = 'Electronics' THEN ROUND(RAND() * 50 + 50, 2) -- Prix aléatoire entre 50 et 100 pour les produits électroniques
        WHEN category = 'Accessories' THEN ROUND(RAND() * 15 + 10, 2) -- Prix aléatoire entre 10 et 25 pour les accessoires
        WHEN category = 'Apparel' THEN ROUND(RAND() * 40 + 20, 2) -- Prix aléatoire entre 20 et 60 pour les vêtements
        WHEN category = 'Footwear' THEN ROUND(RAND() * 50 + 30, 2) -- Prix aléatoire entre 30 et 80 pour les chaussures
        WHEN category = 'Sports Equipment' THEN ROUND(RAND() * 50 + 50, 2) -- Prix aléatoire entre 50 et 100 pour les équipements de sport
        WHEN category = 'Fitness Equipment' THEN ROUND(RAND() * 100 + 100, 2) -- Prix aléatoire entre 100 et 200 pour les équipements de fitness
        ELSE 0.00 -- Si la catégorie n'est pas reconnue, le prix est fixé à 0.00
    END;


